package iteration;

public class MorningCode {

	public static void main(String[] args) {
		//(1)1~50 정수중..
		//7의배수는 sum7에, 8의배수는 sum8에 합하는 코드.(for)
		
	
		int sum7 = 7 ;
		for(int i=1; i <=50; i++);
		if (i % 50 ==0 );{
			sum7 += i ;
		}
		System.out.println("sum:" + sum7);

		
		//(2) 7의배수는 sum7에, 8의배수는 sum8에 합하는코드.(while)
		//종료조건은 7과 8의 공통배수가 나오면 종료.
	}

}
